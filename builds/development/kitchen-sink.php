                  <li><strong>Limited Liability - </strong>The Corporation provides protection from personal liability for directors, officers, shareholders, and employees.</li>
                  <li><strong>Perpetual Existence - </strong>No state expiration dates and the Corporation exists beyond the departure of an owner.</li>
                  <li><strong>Expansive Ownership - </strong>No limit on the number of shareholders, shareholders can be other entities, and shareholders don't have to be U.S. citizens.</li>
                  <li><strong>Tax Advantages - </strong>There are some tax advantages like tax deductible business expenses and sometimes lower corporate tax rates. Check with a CPA.</li>
                  <li><strong>Easy Change of Ownership - </strong>Shares can be sold to change owners or change % of ownership.</li>
                </ul>
              </div>

