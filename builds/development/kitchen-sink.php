            <p>An agent, required to be appointed by a corporation, whose authority is limited to receiving process issued against the corporation. Also known as a Registered Agent or a Resident Agent.</p>

            <strong>Alter Ego</strong>
            <p>A doctrine of law which disregards the principle of limited liability enjoyed by a corporate entity when it is proven that, in fact, no separate identity of the individual and corporation exists.</p>
