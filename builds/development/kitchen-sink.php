          <div class="col-md-6">
            <p class="text-center"><strong>Advantages</strong></p>
            <ul>
              <li><strong>Tax Flexibility - </strong> The IRS doesn't actually recognize an LLC as a distinct entity type so the LLC can be taxed as a Sole Proprietorship for a single member or married couple, a Partnership for an LLC with multiple members, or the LLC can opt to be taxed like a corporation.</li>
              <li><strong>Less Paperwork - </strong>LLC's have less required paperwork than C-Corps and S-Corps. The Operating Agreement and state laws govern the LLC.</li>
              <li><strong>Limited Liability - </strong>Like corporations, LLC's provide the owners with protection from personal liability.</li>
              <li><strong>Ownership Flexibility - </strong> Unlike the S-Corp, the owners of the LLC are not subject to limitations regarding citizenship. The LLC can also list other entities as owners including Corporations, other LLC's and Trusts.</li>
              <li><strong>Avoid "Double Taxation" - </strong>Unless the LLC chooses to be taxed as a corporation, the profits flow through to the owners. This means there is no taxation on the entity level and only on the personal income level.</li>
            </ul>
          </div>
          <div class="col-md-6">
            <p class="text-center"><strong>Disadvantages</strong></p>
            <ul>
              <li><strong>Self-Employment Taxes - </strong> Unless the LLC elects to be taxed like a coporation, the profits that flow through to the members are often taxed at a higher rate than they would be at a corporate level.</li>
              <li><strong>Limited Life - </strong>In many states the LLC can elect to exist "perpetually" when filing the original articles. In other cases the LLC has an expiration date or ceases to exist when a member departs. This can often be resolved by specifying the handling of these events in the Operating Agreement.</li>
              <li><strong>Confusing Structure - </strong>Managers, Members, Managing Members and so on are not defined as well as the Officers and Directors of a corporation. This can lead to confusion unless it is clarified in the Operating Agreement.</li>
            </ul>
